package tiposVariaveis;

public class tiposVariaveis {
    public static void main(String[] args) {
        //textual:
        char tipoChar = 'J';
        char tipoChar1 = 'K';

        String tipoString = "Hello world!";
        String tipoString1 = "Welcome!";

        //numeral:
        byte tipoByte = 1;
        byte tipoByte1 = 0;

        short tipoShort = 3;
        short tipoShort1 = 7;

        int tipoInt = 29;
        int tipoInt1 = 30;

        long tipoLong = 325978402L;
        long tipoLong1 = 325978405L;

        float tipoFloat = 65.5F;
        float tipoFloat1 = 67.5F;

        double tipoDouble = 325.7D;
        double tipoDouble1 = 327.7D;

        //logico:
        boolean tipoBoolean = true;
        boolean tipoBoolean1 = false;

        System.out.println(tipoBoolean);
        System.out.println(tipoBoolean1);
        System.out.println(tipoByte);
        System.out.println(tipoByte1);
        System.out.println(tipoChar);
        System.out.println(tipoChar1);
        System.out.println(tipoDouble);
        System.out.println(tipoDouble1);
        System.out.println(tipoFloat1);
        System.out.println(tipoFloat);
        System.out.println(tipoInt);
        System.out.println(tipoInt1);
        System.out.println(tipoLong);
        System.out.println(tipoLong1);
        System.out.println(tipoString);
        System.out.println(tipoString1);
        System.out.println(tipoShort1);
        System.out.println(tipoShort);

    }
}
